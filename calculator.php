<?php

class Calculator
{

    public function add($a, $b): int
    {
        return $a + $b;
    }

    public function subtract($a, $b): int
    {
        return $a - $b;
    }

    public function multiply($a, $b): int
    {
        return $a * $b;
    }

    public function devide($a, $b): int
    {
        return $a / $b;
    }
}